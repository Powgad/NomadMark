//
// ## 内存管理契约
//
// ### 分配端（Rust）
// - 文档句柄：使用 `Box::into_raw` 转换为原始指针
// - 数组：使用 `Box::leak` 返回稳定指针
// - 字符串内容：使用 `std::alloc::alloc` 或 `Box::leak` 手动分配
//
// ### 释放端（调用者）
// - 文档：调用 `md_document_release`
// - 命令数组：调用 `md_free_commands`
// - TOC：调用 `md_free_toc`
// - 搜索结果：调用 `md_free_search_results`
// - 脏矩形：调用 `md_free_dirty_rects`
//
// ### 调用者责任
// 1. 只能调用一次释放函数
// 2. 释放后指针变为无效
// 3. 必须配对调用创建和释放函数
// 4. 释放前确保没有其他代码持有引用
//
// ### 类型安全
// - 所有导出结构体使用 `#[repr(C)]` 确保 C ABI 兼容性
// - 字符串使用 `(ptr, len)` 对而非 `*const c_char` 以支持 UTF-8
// - 数组使用 `(ptr, len)` 对
// =============================================================================

